export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { adminSecret } = req.body
  if (adminSecret !== process.env.ADMIN_SECRET) {
    return res.status(401).json({ error: 'Unauthorized' })
  }

  const rows = [
    ...STOCKS.map(s => ({ asset_symbol: s.symbol, asset_name: s.name, asset_type: 'stock', is_curated: true })),
    ...ETFS.map(s => ({ asset_symbol: s.symbol, asset_name: s.name, asset_type: 'etf', is_curated: true })),
    ...CRYPTOS.map(s => ({ asset_symbol: s.symbol, asset_name: s.name, asset_type: 'crypto', is_curated: true })),
  ]

  const { error } = await supabase
    .from('asset_directory')
    .upsert(rows, { onConflict: 'asset_symbol' })

  if (error) return res.status(500).json({ error: error.message })

  return res.status(200).json({
    message: `Seeded ${rows.length} assets`,
    stocks: STOCKS.length,
    etfs: ETFS.length,
    cryptos: CRYPTOS.length,
  })
}
